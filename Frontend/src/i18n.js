function readStoredLang() {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored === 'sw' || stored === 'en') return stored;
  } catch {
    /* ignore */
  }
  return 'en';
}

let currentLang = readStoredLang();

function resolve(obj, path) {
  const parts = String(path).split('.');
  let cur = obj;
  for (const p of parts) {
    if (cur == null || typeof cur !== 'object') return undefined;
    cur = cur[p];
  }
  return cur;
}

function interpolate(str, params) {
  if (!params || typeof str !== 'string') return str;
  return str.replace(/\{(\w+)\}/g, (_, key) =>
    params[key] != null ? String(params[key]) : `{${key}}`
  );
}

/** Translate a dotted key. Optional params for `{name}` placeholders. */
export function t(key, params) {
  const primary = resolve(dict[currentLang], key);
  if (typeof primary === 'string') return interpolate(primary, params);
  const fallback = resolve(dict.en, key);
  if (typeof fallback === 'string') return interpolate(fallback, params);
  return key;
}

export function getLang() {
  return currentLang;
}

export function setLang(lang) {
  const next = lang === 'sw' ? 'sw' : 'en';
  if (next === currentLang) return currentLang;
  currentLang = next;
  try {
    localStorage.setItem(STORAGE_KEY, currentLang);
  } catch {
    /* ignore */
  }
  listeners.forEach((cb) => {
    try {
      cb(currentLang);
    } catch {
      /* ignore */
    }
  });
  window.dispatchEvent(new CustomEvent('ushirika:langchange', { detail: { lang: currentLang } }));
  return currentLang;
}

export function toggleLang() {
  return setLang(currentLang === 'en' ? 'sw' : 'en');
}

/** Subscribe to language changes. Returns an unsubscribe function. */
export function onLangChange(cb) {
  if (typeof cb !== 'function') return () => {};
  listeners.add(cb);
  return () => listeners.delete(cb);
}

export function langSwitchHtml(extraClass = '') {
  const other = currentLang === 'en' ? 'SW' : 'EN';
  return `<button type="button" class="lang-switch ${extraClass}" data-lang-toggle aria-label="${t('common.switchLang')}" title="${t('common.switchLang')}">${other}</button>`;
}

